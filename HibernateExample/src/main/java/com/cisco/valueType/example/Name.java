package com.cisco.valueType.example;

public class Name {

    char initial;
    String first;
    String last;

    public String getFirst() {
        return first;
    }

    void setFirst(String first) {
        this.first = first;
    }

    public String getLast() {
        return last;
    }

    void setLast(String last) {
        this.last = last;
    }

    public char getInitial() {
        return initial;
    }

    void setInitial(char initial) {
        this.initial = initial;
    }

}
